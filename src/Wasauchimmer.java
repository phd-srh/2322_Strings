public class Wasauchimmer {
    private String keineAhnung;
    private int schnickSchnack;

    public Wasauchimmer(String keineAhnung, int schnickSchnack) {
        this.keineAhnung = keineAhnung;
        this.schnickSchnack = schnickSchnack;
   }

   public void machWas() {
       System.out.println(keineAhnung + schnickSchnack);
   }
}
